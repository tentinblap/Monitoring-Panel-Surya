    public function login(Request $request)
    {
        $valid = Auth::attempt($request->only('email', 'password'));

        if($valid){
            $user = Auth::user();

            return $user;
        }

        return response()->json(['message' => 'Email & Password doesnt match'], 404);
    }

    public function register(Request $request)
    {
        if($request->name == null || $request->password == null || $request->email == null){
            return response()->json(['message' => 'Silahkan lengkapi data pendaftaran'], 404);
        }
        $user = new User;
        $user->name = $request->name;
        $user->email = $request->email;
        $user->password = Hash::make($request->password);
        $user->save();
        return $user;
    }
